//import mongoose
const mongoose = require("mongoose");

//create a user schema
const PostSchema = new mongoose.Schema(
  {
    title: {
      type: String,
      required: true,
      unique: true,
    },

    desc: {
      type: String,
      required: true,
    },
    photo: {
      type: String,
      required: false,
    },
    username: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

//export the mongoose model and pass your schema
module.exports = mongoose.model("Post", PostSchema);
